                        <h2>Node JS</h2> 
                        <p> Node JS is a Javascript development framework based on Google’s V8 engine. With Node JS on server-side, Javascript is used from client to Server or end-to-end. Node JS web servers are easily scalable as they are based on a unique event handling logic for processing web requests. This framework is easy to  extend and deploy.</p>
                   
                        <h2>MongoDB</h2> 
                        <p> Classically most of the web applications were using RDBMS such as MySQL , POSTGRE etc. These RDBMS databases can store structured data only and are very inefficient when it comes to storing non-structured data like images, comments on social media, blogs etc.

To store such non-structured data, No-SQL databases came into being. MongoDB is one of such popular No-SQL database. It uses JSON( Java Script Object Notation) rather than rows and column to store data. It provides an excellent backend for high traffic websites. MongoDB is easy to learn and scale by adding more nodes or what is called as horizontal scaling.</p>
